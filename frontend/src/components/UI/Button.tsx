import React, { ButtonHTMLAttributes } from 'react';
import { cn } from '../../utils/cn';

interface ButtonProps extends ButtonHTMLAttributes<HTMLButtonElement> {
  variant?: 'primary' | 'secondary' | 'outline';
  size?: 'sm' | 'md' | 'lg';
  children: React.ReactNode;
}

const Button: React.FC<ButtonProps> = ({
  variant = 'primary',
  size = 'md',
  className,
  children,
  ...props
}) => {
  const baseClasses = 'inline-flex items-center justify-center font-medium rounded-lg transition-all duration-200 focus:outline-none focus:ring-2 focus:ring-offset-2 disabled:opacity-50 disabled:cursor-not-allowed';
  
  const variants = {
    primary: 'bg-mediterranean-marroncino hover:bg-opacity-90 text-mediterranean-bianco focus:ring-mediterranean-marroncino shadow-lg hover:shadow-xl',
    secondary: 'bg-mediterranean-beige hover:bg-opacity-80 text-mediterranean-blu-scuro focus:ring-mediterranean-beige',
    outline: 'border-2 border-mediterranean-marroncino text-mediterranean-marroncino hover:bg-mediterranean-marroncino hover:text-mediterranean-bianco focus:ring-mediterranean-marroncino',
  };

  const sizes = {
    sm: 'px-3 py-1.5 text-sm',
    md: 'px-4 py-2 text-base',
    lg: 'px-6 py-3 text-lg',
  };

  return (
    <button
      className={cn(baseClasses, variants[variant], sizes[size], className)}
      {...props}
    >
      {children}
    </button>
  );
};

export default Button;